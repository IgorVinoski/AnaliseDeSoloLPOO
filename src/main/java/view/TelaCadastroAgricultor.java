    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
     String nome = txfNome.getText().trim();
        String telefone = txfTelefone.getText().trim();
        String email = txfEmail.getText().trim();

        if (nome.isEmpty() || telefone.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos!");
            return;
        }

        // Cria o objeto Agricultor com os dados
        Agricultor agricultor = new Agricultor(nome, telefone, email);

        // Utiliza a PersistÃªnciaJPA para salvar os dados
        PersistenciaJPA persistencia = new PersistenciaJPA();
        try {
            persistencia.persist(agricultor);
            JOptionPane.showMessageDialog(this, "Agricultor cadastrado com sucesso!");

            // Atualiza a lista de agricultores na interface
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar agricultor: " + e.getMessage());
        } finally {
            persistencia.fecharConexao();
        }

        // Limpar os campos
        txfNome.setText("");
        txfTelefone.setText("");
        txfEmail.setText("");
    }//GEN-LAST:event_btnCadastrarActionPerformed

    
    


